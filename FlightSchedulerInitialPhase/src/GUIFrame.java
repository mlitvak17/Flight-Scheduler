    private void FlightStatusjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FlightStatusjButtonActionPerformed
    BookingQueries booking = new BookingQueries();
    String FLIGHT = (String) FlightjComboBox1.getSelectedItem();
    Date DATE = Date.valueOf(DatejComboBox1.getSelectedItem().toString()); 
    booking.getStatusByFlightDay(FLIGHT, DATE);
    System.out.print(booking.getStatusByFlightDay(FLIGHT, DATE));
    String bookingStatusList = String.join(", ", booking.getStatusByFlightDay(FLIGHT, DATE));
    String bookingStatus = "Customer(s) " + bookingStatusList + " are booked for " + FLIGHT + " on " + DATE;
    FlightStatusjTextField.setText(bookingStatus);
    }//GEN-LAST:event_FlightStatusjButtonActionPerformed

