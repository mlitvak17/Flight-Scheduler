    private void BookFlightjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BookFlightjButtonActionPerformed
       BookingQueries booking = new BookingQueries();
       String CUSTOMER = (String) CustomerjComboBox.getSelectedItem();
       String FLIGHT = (String) FlightjComboBox.getSelectedItem();
       Date DATE = Date.valueOf(DatejComboBox.getSelectedItem().toString()); 
       booking.addBooking(CUSTOMER, FLIGHT, DATE);
       String customerBooked = CUSTOMER + " is booked for flight " + FLIGHT + " on " + DATE;
       String customerWaitlisted = "'"+ CUSTOMER + "'" + " is waitlisted for flight " + FLIGHT + " on " + DATE;
       if (booking.getBookingStatus())
       {
           BookingStatusjTextField.setText(customerBooked);
       }
       else if (booking.getWaitlistStatus())
       {
           BookingStatusjTextField.setText(customerWaitlisted);
       }
    }//GEN-LAST:event_BookFlightjButtonActionPerformed

